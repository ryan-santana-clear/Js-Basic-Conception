/**
 * Structures Condition Switch
 */

let cor = "Purple";

switch(cor){

    case "Blue":
        console.log("Sky");
        break;

    case "Green":
        console.log("Earth");
        break;

    case "Yellow":
        console.log("Sun");
        break;

    case "Purple":
        console.log("EggPlant");
        break;

    default:
        console.log("Alien"); // Don't use BREAK!

}